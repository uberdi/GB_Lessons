int num1=3;
int num2=5;

// "&&"- "И" (верны оба условия) логическое умножение
// "||" это или (логическое сложение)
if (num1>7 && num2>7) //при двух амперсандах YES будет написано только в случае если и левая и правая половина(условия) выполняются
{
    Console.Write("Yes");
}
else
{
    Console.Write("No");
}